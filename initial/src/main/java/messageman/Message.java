package messageman;

public class Message {
	
	private String name;
	
	public Message() {
		
	}
	
	

	/**
	 * @param name
	 */
	public Message(String name) {
		super();
		this.name = name;
	}



	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}


}
